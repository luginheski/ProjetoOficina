    private void jBtnConsultaCpfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultaCpfActionPerformed
        ClienteDao dao = new ClienteDao();
        boolean status = dao.conectar();

        if (status == true) {
            Cliente cliente = dao.consulta(jTxtCpf.getText().replace(".", "").replace("-", ""));

            if (cliente == null) {
                JOptionPane.showMessageDialog(null, "Cliente não localizado");
            } else {
                jTxtCpf.setEditable(false);
                jTxtCpf.setText(String.valueOf(cliente.getCpf()));
                jTxtNome.setText(cliente.getNome());
                jBtnSalvar.setEnabled(true);
            }
            dao.desconectar();
        } else {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        }
    }//GEN-LAST:event_jBtnConsultaCpfActionPerformed

    private void jBtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSalvarActionPerformed
        Veiculo vel = new Veiculo();
        VeiculoDao dao = new VeiculoDao();
        boolean status;
        int resposta;

        status = dao.conectar();

        if (status == false) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        } else {
            validaDados();
            vel.setFabricante(jTxtFabricante.getText());
            vel.setModelo(jTxtModelo.getText());
            vel.setAnoFabricacao(Integer.parseInt(jTxtAnoFabricacao.getText()));
            vel.setAnoModelo(Integer.parseInt(jTxtAnoModelo.getText()));
            vel.setHodometroAnterior(Integer.parseInt(jTxtHodometroAnterior.getText()));
            //vel.setHodometroAtual(Integer.parseInt(jTxtHodometroAnterior.getText()));
            vel.setPlaca(jTxtPlaca.getText());
            Cliente cpf = new Cliente();
            cpf.setCpf(jTxtCpf.getText().replace(".", "").replace("-", ""));
            vel.setCpfCliente(cpf);
            resposta = dao.salvar(vel);         

            if (resposta == 1) {
                JOptionPane.showMessageDialog(null, "Dados incluidos com sucesso");
                   limpaTela();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar inserir dados");
            }
        }
        dao.desconectar();
    }//GEN-LAST:event_jBtnSalvarActionPerformed

    private void jBtnConsultaVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultaVeiculoActionPerformed
        VeiculoDao dao = new VeiculoDao();
        boolean status = dao.conectar();

        if (status == true) {
            Veiculo vel = dao.consultaPlaca(jTxtPlaca.getText().replace(".", "").replace("-", ""));
            if (vel == null) {
                JOptionPane.showMessageDialog(null, "Placa não localizada");
            } else {
                jTxtNome.setText(vel.getCpfCliente().getNome());
                jTxtCpf.setText(vel.getCpfCliente().getCpf());
                jTxtFabricante.setText(vel.getFabricante());
                jTxtModelo.setText(vel.getModelo());
                jTxtAnoFabricacao.setText(String.valueOf(vel.getAnoFabricacao()));
                jTxtAnoModelo.setText(String.valueOf(vel.getAnoModelo()));
                jTxtPlaca.setText(vel.getPlaca());
                jTxtHodometroAnterior.setText(String.valueOf(vel.getHodometroAnterior()));
                jTxtIdVel.setText(String.valueOf(vel.getIdVeiculo()));
                abilitaEdicao();
                jBtnSalvar.setEnabled(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        }
    }//GEN-LAST:event_jBtnConsultaVeiculoActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        String placa;
        VeiculoDao dao = new VeiculoDao();
        boolean status = dao.conectar();

        if (status == false) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        } else {
            placa = jTxtPlaca.getText();
            boolean operacao = dao.excluir(placa);
            if (operacao == true) {
                JOptionPane.showMessageDialog(null, "Veiculo excluído com sucesso!");
                this.limpaTela();
            } else {
                JOptionPane.showMessageDialog(null, "Erro na exclusão do veiculo");
            }
            dao.desconectar();
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
        Veiculo vel = new Veiculo();
        VeiculoDao dao = new VeiculoDao();
        boolean status;
        int resposta;
        
        vel.setIdVeiculo(Integer.parseInt(jTxtIdVel.getText()));
        vel.setFabricante(jTxtFabricante.getText());
        vel.setModelo(jTxtModelo.getText());
        vel.setAnoFabricacao(Integer.parseInt(jTxtAnoFabricacao.getText()));
        vel.setAnoModelo(Integer.parseInt(jTxtAnoModelo.getText()));
        vel.setHodometroAnterior(Integer.parseInt(jTxtHodometroAnterior.getText()));
        vel.setPlaca(jTxtPlaca.getText());
                    
            status = dao.conectar();

        if (status == false) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        } else {
            resposta = dao.atualizar(vel);
            
            if (resposta == 1) {
                JOptionPane.showMessageDialog(null, "Dados incluidos com sucesso");
                limpaTela();
            } else if (resposta == 1062) {
                JOptionPane.showMessageDialog(null, "Matricula já foi cadastrada");
            }  else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar inserir dados");
            }
        }
        dao.desconectar();
    }//GEN-LAST:event_jBtnEditarActionPerformed

