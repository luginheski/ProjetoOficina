    public void validaDados() {
        String fabricante = jTxtFabricante.getText();
        String modelo = jTxtModelo.getText();
        String anoFabricacao = jTxtAnoFabricacao.getText();
        String anoModelo = jTxtAnoModelo.getText();
        String placa = jTxtPlaca.getText();
        String hodometro = jTxtHodometroAnterior.getText();

        if (fabricante.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe um fabricante");
        } else if (modelo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "informe um modelo");
        } else if (anoFabricacao.isEmpty()) {
            JOptionPane.showMessageDialog(null, "informe um anoFabricacao");
        } else if (anoModelo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "informe uma anoModelo");
        } else if (placa.isEmpty()) {
            JOptionPane.showMessageDialog(null, "informe um placa");
        } else if (hodometro.isEmpty()) {
            JOptionPane.showMessageDialog(null, "informe um hodometro");
        } else {
            JOptionPane.showMessageDialog(null, "Veiculo cadastrado" + "\nFabricante: "
                    + fabricante + "\nModelo: " + modelo + "\nAno Fabricação: " + anoFabricacao
                    + "\nAno Modelo: " + anoModelo + "\nPlaca: " + placa + "\nKm: " + hodometro);
        }
    }

    private void preencherTabela(String placa) {
        VeiculoDao dao = new VeiculoDao();
        boolean status = dao.conectar();

        if (status == false) {
            JOptionPane.showMessageDialog(null, "Erro de conexão");
        } else {
            List<Veiculo> listaVel = dao.listar(placa);

            DefaultTableModel tabelaVel = (DefaultTableModel) jTabVeiculo.getModel();
            jTabVeiculo.setRowSorter(new TableRowSorter(tabelaVel));
            tabelaVel.setNumRows(0);

            for (Veiculo v : listaVel) {
                Object[] obj = new Object[]{
                    v.getCpfCliente().getNome(),
                    v.getCpfCliente().getCpf(),
                    v.getFabricante(),
                    v.getModelo(),
                    v.getAnoFabricacao(),
                    v.getAnoModelo(),
                    v.getPlaca(),
                    v.getHodometroAnterior()
                };
                tabelaVel.addRow(obj);
            }
            dao.desconectar();
        }
    }

